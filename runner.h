
#ifndef __RUNNER_H__
#define __RUNNER_H__

#include <stdio.h>

extern int tests_run;

#define FAIL() printf("\nfailure in %s() line %d\n", __func__, __LINE__)
#define _assert(test) do { if (!(test)) { FAIL(); return 1; } } while(0)
#define _verify(test) do { int r=test(); tests_run++; if(r) return r; } while(0)

#endif

